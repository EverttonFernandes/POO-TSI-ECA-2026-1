package Aula04AssociacaoAgregacaoComposicao.ExemploComposicao;

public class Motor {
    String modelo;
    String marca;

    public Motor(String modelo, String marca) {
        this.modelo = modelo;
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public String getMarca() {
        return marca;
    }
}
